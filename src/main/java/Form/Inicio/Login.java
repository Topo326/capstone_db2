    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        String username = InputUsername.getText().trim();
        String password = new String(InputPassword.getPassword());

        if (username.isEmpty() || password.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        com.controller.LoginController login = new com.controller.LoginController();
        com.model.User user = login.authenticate(username, password);

        if (user != null) {
            com.controller.util.SessionData.setLoggedUser(user);
            javax.swing.JOptionPane.showMessageDialog(this, "Welcome, " + user.getUsername() + "!");

            Task taskFrame = new Task();
            taskFrame.setVisible(true);
            this.dispose();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid username or password.");
        }
    }//GEN-LAST:event_LoginButtonActionPerformed

